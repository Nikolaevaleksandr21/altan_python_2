# $$$ Словарь (dict) $$$

# пара "ключ-значение"
# {ключ_1:значение_1, ключ_2:значение_2}

# Создание словаря
my_dict = {10:3.14, "abc":[1,2,3], 'A':"python"}

# Чтение значений
val = my_dict["abc"]

# замена значения
my_dict[10] = 1000

# добавление новой пары
my_dict["hi"] = "hello"

# удаление пары по ключу
del my_dict["hi"]

