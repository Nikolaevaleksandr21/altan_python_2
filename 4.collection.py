# Срез списка

# создание исходного списка из строки
s = "Hello, world!"
my_list = list(s)

# срез от начала до конца
my_slice = my_list[:]

# срез от индекса 2 до конца исходного списка

my_slice = my_list[2:]

# срез с начала списка до 5-го индекса не включительно
my_slice = my_list[:5]

# срез с 3-го индекса до 7-го индекса не включительно
my_slice = my_list[3:7]

# срез от начала до конца списка с шагом 2
my_slice = my_list[::2]

# срез от индекса 2 до конца исходного списка с шагом 2
my_slice = my_list[2::2]

# срез от индекса 3 до 10 индекса с шагом 3
my_slice = my_list[3:10:3]

# срез от my_list-2 до -9 не вкл с шагом 2
my_slice = my_list[-2:-9:-2]

# переворот списка срезом
my_slice = my_list[::-1]

